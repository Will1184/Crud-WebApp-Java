package modelo;

/**
 *
 * @author brand
 */
public class Cargo {
    private int id;
    private String nombreCargo;
    private boolean estado;

    public Cargo() {
    }

    public Cargo(int id, String nombreCargo, boolean estado) {
        this.id = id;
        this.nombreCargo = nombreCargo;
        this.estado = estado;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombreCargo() {
        return nombreCargo;
    }

    public void setNombreCargo(String nombreCargo) {
        this.nombreCargo = nombreCargo;
    }

    public boolean isEstado() {
        return estado;
    }

    public void setEstado(boolean estado) {
        this.estado = estado;
    }
    
    
}
